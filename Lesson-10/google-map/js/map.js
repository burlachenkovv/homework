navigator.geolocation.getCurrentPosition(updatePosition);

function updatePosition(pos) {

	let position = new google.maps.LatLng(pos.coords.latitude, pos.coords.longitude);

	let options = {
		zoom: 15,
		center: position,
		mapTypeId: google.maps.MapTypeId.ROADMAP,
		styles: mapStyles()
	};

	let map = new google.maps.Map(document.getElementById("map"), options);

	let markerImage = "./img/pointer.png";
	let marker = new google.maps.Marker({
		position: position,
		map: map,
		icon: markerImage,
		animation: google.maps.Animation.DROP
	});
	marker.addListener('mouseover', () => marker.setAnimation(google.maps.Animation.BOUNCE));
	marker.addListener('mouseout', () => marker.setAnimation(null));

	function toggleBounce() {
		if (marker.getAnimation() !== null) {
			marker.setAnimation(null);
		} else {
			marker.setAnimation(google.maps.Animation.BOUNCE);
		}
	}
}


